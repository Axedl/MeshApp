export const UPGRADE_MAP: Map<string, UpgradeDef> = new Map(
  ALL_UPGRADES.map(u => [u.id, u])
);

export function getUpgrade(id: string): UpgradeDef | undefined {
  return UPGRADE_MAP.get(id);
}

export function getUpgradesByCategory(category: UpgradeCategory): UpgradeDef[] {
  return ALL_UPGRADES.filter(u => u.category === category);
}

export function getUpgradesByPath(path: CareerPath, category?: UpgradeCategory): UpgradeDef[] {
  return ALL_UPGRADES.filter(u =>
    u.path === path && (category == null || u.category === category)
  );
}
