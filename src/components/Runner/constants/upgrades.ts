import type { CareerPath, RunnerAct } from '../../../types';

export type UpgradeCategory =
  | 'act1_universal'
  | 'act2_hustle'
  | 'act2_path'
  | 'act3_base'
  | 'act3_branch_a'
  | 'act3_branch_b'
  | 'act4_path'
  | 'ghost_universal'
  | 'ghost_path';

export type CostResource = 'eddies' | 'secondary' | 'influence' | 'contacts' | 'rep' | 'ghost_tokens';

export interface UpgradeDef {
  id: string;
  name: string;
  flavour: string;
  stat: string;
  category: UpgradeCategory;
  act: RunnerAct;
  path?: CareerPath;
  branch?: 'a' | 'b';
  cost: number;
  costResource: CostResource;
  secondaryCost?: number;
  // Income modifiers
  incomeMod?: number;         // flat eddie/s addition
  incomeMultMod?: number;     // multiplier stacked on income
  secondaryGenMod?: number;   // secondary resource/s addition
  secondaryIncomeMod?: number;// flat eddie/s per secondary resource held
  influenceGenMod?: number;   // influence/s addition
  offlineCapHours?: number;   // ghost memory offline cap
  prereq?: string[];          // upgrade IDs required before this
  maxLevel?: number;          // default 1 (one-time)
}

